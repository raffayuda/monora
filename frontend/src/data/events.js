export function getEventById(id) {
  return allEvents.find(event => event.id === Number(id))
}

export function getEventBySlug(slug) {
  return allEvents.find(event => event.slug === slug)
}

export function getEventsByCategory(category) {
  if (category === 'All') return allEvents
  return allEvents.filter(event => event.category === category)
}

export function getFeaturedEvents() {
  return allEvents.filter(event => event.tags.length > 0).slice(0, 6)
}

// Get unique provinces with event counts
export function getProvinces() {
  const provinceMap = {}
  allEvents.forEach(event => {
    if (!provinceMap[event.province]) {
      provinceMap[event.province] = { name: event.province, count: 0, events: [] }
    }
    provinceMap[event.province].count++
    provinceMap[event.province].events.push(event)
  })
  return Object.values(provinceMap)
}

// Get events by city
export function getEventsByCity(city) {
  return allEvents.filter(event => event.city === city)
}

// Get events by province
export function getEventsByProvince(province) {
  return allEvents.filter(event => event.province === province)
}

// Calculate discounted price
export function getDiscountedPrice(price, discount) {
  if (!discount) return price
  return Math.round(price * (1 - discount.percentage / 100) * 100) / 100
}
